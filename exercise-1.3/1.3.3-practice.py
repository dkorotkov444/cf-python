# Exercise 1.3. Code Practice 3: Loops

# Print numbers from 10 to 50 (inclusive) in steps of 10 using for loop
for i in range(10, 60, 10):
    print(i)
print("And we're done!")

# Print numbers from 10 to 50 (inclusive) in steps of 10 using while loop
num = 10
while num <= 50:
    print(num)
    num += 10
print("And we're done!")
